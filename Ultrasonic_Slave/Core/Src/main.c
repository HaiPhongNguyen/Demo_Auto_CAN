/* USER CODE BEGIN PFP */
float MovingAvg_Update(MovingAvg_t *f, float new_sample)
{
    // Trừ giá trị cũ
    f->sum -= f->buffer[f->index];

    // Ghi giá trị mới
    f->buffer[f->index] = new_sample;

    // Cộng vào sum
    f->sum += new_sample;

    // Tăng index vòng
    f->index = (f->index + 1) % FILTER_SIZE;

    // Đếm số mẫu hợp lệ
    if (f->count < FILTER_SIZE)
        f->count++;

    // Trả về trung bình
    return f->sum / f->count;
}
